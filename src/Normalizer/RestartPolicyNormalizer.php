        }
        if ($data->isInitialized('maximumRetryCount') && null !== $data->getMaximumRetryCount()) {
            $dataArray['MaximumRetryCount'] = $data->getMaximumRetryCount();
        }
        foreach ($data as $key => $value) {
            if (preg_match('/.*/', (string) $key)) {
